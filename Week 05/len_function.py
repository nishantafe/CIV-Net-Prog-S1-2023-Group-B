countries = ["Australia", "USA", "Japan"]
word = "Magical"
people = {"John": "Smith", "George": "Michael"}

print("Number of countries:", len(countries))  # The length (number) of items in the list
print("Number of letters:", len(word))  # The length (number) of letters in the word
print("Number of people:", len(people))  # The length (number) of people (items) in the dictionary

# Slicing
print("The second country in the list is:", countries[1])
print("The second letter in the word is:", word[1])
